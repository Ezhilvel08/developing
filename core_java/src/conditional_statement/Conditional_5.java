package conditional_statement;

public class Conditional_5 {
	
	public static void condition5() {
		String fruit="banana";
	switch (fruit) {
	case "mango":
		System.out.println("my favourit fruit is mango");
		break;
	case "orange":
		System.out.println("my favourit fruit is orange");
		break;
	case "apple":
		System.out.println("my favourit fruit is apple");
		break;
	case "pineapple":
		System.out.println("my favourit fruit is pineapple");
		break;
	case "watermelon":
		System.out.println("my favourit fruit is watermelon");
		break;
	case "grapes":
		System.out.println("my favourit fruit is grapes");
		break;
	default:
		System.out.println("my favourit fruit is not available");
		break;
	}
	}

	public static void main(String[] args) {
		condition5();
	}

}
