package conditional_statement;

public class Condition_2 {

	public static void condition2() {

		int a = 100;
		if (a >= 50) {
			System.out.println("a is greater than 50");
		}
		if (a >= 75) {
			System.out.println("a is greater than 75");
		}
		if (a == 100) {
			System.out.println("a is equal to 100");
		}
		if (a != 100) {
			System.out.println("a is not equal to 100");
		}

	}

	public static void main(String[] args) {

		condition2();
	}

}
