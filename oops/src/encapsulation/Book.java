package encapsulation;

public class Book {
	
	int id;
	String name;
	String author;
	String pub;
	int quantity;
	
	public Book(int id, String name, String author, String pub, int quantity) {
		super();
		this.id = id;
		this.name = name;
		this.author = author;
		this.pub = pub;
		this.quantity = quantity;
	}
	

	
	

}
